#include <iostream>

using namespace std;

int main() {
    
    int x;
    int y;

    cout << "Enter the first number: ";
    cin >> x;
    cout << "Enter the second number: ";
    cin >> y;

    cout << "The addition of both numbers is: " << x + y << endl;
    cout << "The subtraction of both numbers is: " << x - y << endl;
    cout << "The multiplication of both numbers is: " << x * y << endl;
    cout << "The division of both numbers is: " << x / y << endl;
    return 0;
}